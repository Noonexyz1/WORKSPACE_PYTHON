#En python las cadenas no INMUTABLES
fact = "La luna no tiene atmosfera"
segundo_fact = fact + " No se puede escuchar sonidos"
print(fact)
print(segundo_fact)

#formas de usar las "" en cadenas
valor = 'Esto es un comentario con "comillas"'
print(valor)

valor2 = "Esto es el nombre de la marca Ely's, enfacis en el apostrofe"
print(valor2)

valor3 = """We only see about 60% of the Moon's surface, this is known as the "near side"."""
print(valor3)

#multilineas
multiline = "Facts about the Moon:\n There is no atmosphere.\n There is no sound."
print(multiline)

multiline = """Facts about the Moon:
 There is no atmosphere. 
 There is no sound."""
print(multiline)

